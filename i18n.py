_lang_cache: str | None = None


def tr(key: str, **kwargs) -> str:
    """現在の言語設定に合わせてUI文字列を返す。キーワード引数で書式置換も可能。"""
    global _lang_cache
    if _lang_cache is None:
        from settings import load_settings
        _lang_cache = load_settings().get("language", "ja")
    strings = _STRINGS.get(_lang_cache, _STRINGS["ja"])
    text = strings.get(key, _STRINGS["ja"].get(key, key))
    if kwargs:
        text = text.format(**kwargs)
    return text


def reset_lang_cache():
    """言語設定変更後に呼ぶとキャッシュをクリアし次回 tr() で再読み込みする。"""
    global _lang_cache
    _lang_cache = None
